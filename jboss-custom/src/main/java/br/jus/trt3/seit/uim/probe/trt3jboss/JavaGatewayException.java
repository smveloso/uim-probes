public class JavaGatewayException extends Exception {

    public JavaGatewayException() {
    }

    public JavaGatewayException(String message) {
        super(message);
    }

    public JavaGatewayException(String message, Throwable cause) {
        super(message, cause);
    }

    public JavaGatewayException(Throwable cause) {
        super(cause);
    }

}
